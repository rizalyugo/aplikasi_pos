package rstudio.modul;

import java.awt.Graphics;
import java.awt.Graphics2D;
import java.awt.Image;
import javax.swing.ImageIcon;
import javax.swing.JPanel;

/**
 *
 * @author Dendi pradigta <awak.dendi@gmail.com>
 */
public class PanelBackgroundImage1 extends JPanel {

    private Image image;

    public PanelBackgroundImage1() {
        image = new ImageIcon(getClass().getResource("/rstudio/image/bgblue.png")).getImage();
    }
 
    @Override
    protected void paintComponent(Graphics grphcs) {
        super.paintComponent(grphcs);

        Graphics2D gd = (Graphics2D) grphcs.create();
        gd.drawImage(image, 0, 0, getWidth(), getHeight(), null);
        gd.dispose();
    }
}
